/**
 * Generate aggregate CEM CSS entrypoints from generated token CSS files.
 *
 * Usage:
 *   node scripts/generate-css-index.mjs
 */

import fs from "node:fs/promises";
import path from "node:path";

const packageRoot = process.cwd();
const cssDir = path.join(packageRoot, "dist/lib/css");
const importIndexPath = path.join(cssDir, "cem.css");
const combinedPath = path.join(cssDir, "cem-combined.css");

const cssFiles = [
    "cem-colors.css",
    "cem-dimension.css",
    "cem-breakpoints.css",
    "cem-coupling.css",
    "cem-controls.css",
    "cem-shape.css",
    "cem-layering.css",
    "cem-stroke.css",
    "cem-voice-fonts-typography.css",
    "cem-timing.css",
];

async function assertGeneratedCssExists(fileName) {
    const filePath = path.join(cssDir, fileName);
    try {
        await fs.access(filePath);
    } catch (err) {
        throw new Error(`Cannot include missing generated CSS file: ${path.relative(packageRoot, filePath)}`, {
            cause: err,
        });
    }
}

async function readGeneratedCss(fileName) {
    const filePath = path.join(cssDir, fileName);
    return {
        fileName,
        css: await fs.readFile(filePath, "utf8"),
    };
}

async function main() {
    await fs.mkdir(cssDir, { recursive: true });
    for (const fileName of cssFiles) {
        await assertGeneratedCssExists(fileName);
    }

    const importIndexCss = [
        "/* Generated by scripts/generate-css-index.mjs. Do not edit by hand. */",
        ...cssFiles.map((fileName) => `@import url("./${fileName}");`),
        "",
    ].join("\n");

    const cssEntries = [];
    for (const fileName of cssFiles) {
        cssEntries.push(await readGeneratedCss(fileName));
    }

    const combinedCss = [
        "/* Generated by scripts/generate-css-index.mjs. Do not edit by hand. */",
        ...cssEntries.flatMap(({ fileName, css }) => [
            "",
            `/* ${fileName} */`,
            css.trimEnd(),
        ]),
        "",
    ].join("\n");

    await fs.writeFile(importIndexPath, importIndexCss, "utf8");
    await fs.writeFile(combinedPath, combinedCss, "utf8");

    console.log(`Generated ${path.relative(packageRoot, importIndexPath)} (${cssFiles.length} imports)`);
    console.log(`Generated ${path.relative(packageRoot, combinedPath)} (${cssFiles.length} files combined)`);
}

main().catch((err) => {
    console.error(err);
    process.exit(2);
});
