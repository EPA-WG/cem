/**
 * Validates all CEM semantic fixtures in examples/semantic/ and emits
 * dist/cem-dom.report.{md,json}.  Exits non-zero if any fixture has hard
 * violations (severity === 'error').
 *
 * Depends on: packages/cem-dom/dist/index.js (built output).
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readdirSync } from 'node:fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageRoot = join(__dirname, '..');
const workspaceRoot = join(packageRoot, '../..');
const fixturesDir = join(workspaceRoot, 'examples/semantic');
const distDir = join(packageRoot, 'dist');

const { parse, validate } = await import('../dist/index.js');

const fixtureFiles = readdirSync(fixturesDir)
  .filter(f => f.endsWith('.html'))
  .sort();

const generated = new Date().toISOString();
const fixtureResults = [];
let totalErrors = 0;

for (const file of fixtureFiles) {
  const html = await readFile(join(fixturesDir, file), 'utf8');
  const doc = parse(html, file);
  const messages = validate(doc);
  const errors = messages.filter(m => m.severity === 'error');
  const warnings = messages.filter(m => m.severity === 'warning');
  totalErrors += errors.length;

  fixtureResults.push({
    file,
    cemNodes: doc.cemNodes.length,
    parseErrors: doc.errors.length,
    errors: errors.map(m => ({ rule: m.rule, message: m.message, line: m.loc.line, column: m.loc.column })),
    warnings: warnings.map(m => ({ rule: m.rule, message: m.message, line: m.loc.line, column: m.loc.column })),
  });

  const status = errors.length === 0 ? '✓ clean' : `✗ ${errors.length} error(s)`;
  console.log(`  ${file}: ${doc.cemNodes.length} cem nodes, ${status}`);
}

const cleanCount = fixtureResults.filter(r => r.errors.length === 0).length;

const report = {
  generator: 'cem-dom:validate-fixtures',
  generated,
  summary: {
    total: fixtureFiles.length,
    clean: cleanCount,
    hardViolations: totalErrors,
  },
  fixtures: fixtureResults,
};

await mkdir(distDir, { recursive: true });
await writeFile(join(distDir, 'cem-dom.report.json'), JSON.stringify(report, null, 2) + '\n', 'utf8');

const mdLines = [
  '# CEM DOM Fixture Validation Report',
  '',
  `Generated: ${generated}  `,
  '',
  '## Summary',
  '',
  '| Stat | Count |',
  '| ---- | ----- |',
  `| Fixtures checked | ${fixtureFiles.length} |`,
  `| Clean fixtures | ${cleanCount} |`,
  `| Hard violations | ${totalErrors} |`,
  '',
  '## Fixtures',
];

for (const r of fixtureResults) {
  const status = r.errors.length === 0 ? '✓ clean' : `✗ ${r.errors.length} error(s)`;
  mdLines.push('', `### ${r.file}`, '', `CEM nodes: ${r.cemNodes}  `, `Status: ${status}  `);
  if (r.errors.length > 0) {
    mdLines.push('', '**Errors:**', '');
    for (const e of r.errors) {
      mdLines.push(`- \`${e.rule}\` (${e.line}:${e.column}): ${e.message}`);
    }
  }
  if (r.warnings.length > 0) {
    mdLines.push('', '**Warnings:**', '');
    for (const w of r.warnings) {
      mdLines.push(`- \`${w.rule}\` (${w.line}:${w.column}): ${w.message}`);
    }
  }
}

mdLines.push('', '> Generated by `cem-dom:validate-fixtures`. Do not edit by hand.');

await writeFile(join(distDir, 'cem-dom.report.md'), mdLines.join('\n') + '\n', 'utf8');

console.log(`\nValidation complete. Hard violations: ${totalErrors}`);
if (totalErrors > 0) {
  console.error(`FAIL: ${totalErrors} hard violation(s) found.`);
  process.exit(1);
}
